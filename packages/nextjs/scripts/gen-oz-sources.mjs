// Regenerates lib/solc/oz-sources.ts — a vendored map of the OpenZeppelin
// Solidity sources the in-browser compiler needs, keyed by the exact path solc
// requests.
//
// Why: the solc web-worker compiles in the browser with no filesystem, so a
// lab contract's real `import "@openzeppelin/contracts/..."` lines can't hit
// node_modules. We crawl the import graph from the entry points below, snapshot
// every file as a string, and resolve imports against that map before compiling.
//
// Run from packages/nextjs:  node scripts/gen-oz-sources.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const here = path.dirname(fileURLToPath(import.meta.url));
const nextjsRoot = path.resolve(here, "..");

// OZ lives in the hardhat workspace's deps; check the likely roots in order.
const candidates = [
  path.join(nextjsRoot, "node_modules"),
  path.resolve(nextjsRoot, "../hardhat/node_modules"),
  path.resolve(nextjsRoot, "../../node_modules"),
];
const OZ_ROOT = candidates.find(c => fs.existsSync(path.join(c, "@openzeppelin/contracts/package.json")));
if (!OZ_ROOT) throw new Error("Could not find @openzeppelin/contracts in node_modules");

const OUT = path.join(nextjsRoot, "lib/solc/oz-sources.ts");

// Everything the current labs import. Add entry points here when a future lab
// needs more of OZ, then re-run this script.
const entryPoints = [
  "@openzeppelin/contracts/token/ERC20/ERC20.sol",
  "@openzeppelin/contracts/access/Ownable.sol",
];

const collected = new Map();
const importRe = /import\s+(?:\{[^}]*\}\s+from\s+)?["']([^"']+)["']/g;

function resolve(fromKey, importPath) {
  if (importPath.startsWith("@")) return importPath;
  const dir = path.posix.dirname(fromKey);
  return path.posix.normalize(path.posix.join(dir, importPath));
}

function crawl(key) {
  if (collected.has(key)) return;
  const src = fs.readFileSync(path.join(OZ_ROOT, key), "utf8");
  collected.set(key, src);
  let m;
  importRe.lastIndex = 0;
  while ((m = importRe.exec(src))) crawl(resolve(key, m[1]));
}

for (const e of entryPoints) crawl(e);

const ozVersion = JSON.parse(
  fs.readFileSync(path.join(OZ_ROOT, "@openzeppelin/contracts/package.json"), "utf8"),
).version;

const header = `// AUTO-GENERATED by scripts/gen-oz-sources.mjs — do not edit by hand.
// Vendored OpenZeppelin v${ozVersion} sources keyed by the exact import path solc
// requests, so a lab's real \`import "@openzeppelin/contracts/..."\` lines
// resolve in-browser with no network and no flattening.

export const OZ_SOURCES: Record<string, string> = {
`;
let body = "";
for (const [k, v] of collected) body += `  ${JSON.stringify(k)}: ${JSON.stringify(v)},\n`;
fs.writeFileSync(OUT, header + body + "};\n");

console.log(`Wrote ${collected.size} OZ v${ozVersion} files to lib/solc/oz-sources.ts`);
